using Microsoft.EntityFrameworkCore;
using Snapbean.DevDay2024.IdentityApi;

var builder = WebApplication.CreateBuilder(args);

// Setup AuthN / AuthZ
builder.Services.AddAuthorization();

// Setup database
builder.Services.AddDbContext<DatabaseContext>(options =>
{
    options.UseInMemoryDatabase("IdentityDatabase");
});

// Wire up identity-services with db and endpoints
builder.Services
    .AddIdentityApiEndpoints<UserEntity>()
    .AddEntityFrameworkStores<DatabaseContext>();

// Add services to the container.
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

var app = builder.Build();

// Map identity endpoints
app.MapGroup("/account").MapIdentityApi<UserEntity>();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();
app.UseAuthorization();

var summaries = new[]
{
    "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
};

app.MapGet("/weatherforecast", () =>
    {
        var forecast = Enumerable.Range(1, 5).Select(index =>
                new WeatherForecast
                (
                    DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
                    Random.Shared.Next(-20, 55),
                    summaries[Random.Shared.Next(summaries.Length)]
                ))
            .ToArray();
        return forecast;
    })
    .WithName("GetWeatherForecast")
    .RequireAuthorization() // Enforce authorization
    .WithOpenApi();

app.Run();

record WeatherForecast(DateOnly Date, int TemperatureC, string? Summary)
{
    public int TemperatureF => 32 + (int)(TemperatureC / 0.5556);
}